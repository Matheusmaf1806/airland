function getFacilityData(key) {
  return facilitiesMap[key] || null;
}

function processFacility(item) {
  const found = getFacilityData(item);
  if (found) {
    console.log("Tradução:", found.pt);
    console.log("Ícone:", found.icon);
    // Cria um elemento <i> com a classe do ícone do Font Awesome
    const iconElement = document.createElement('i');
    iconElement.className = found.icon;
    
    // Cria um container para exibir o ícone e a tradução
    const container = document.createElement('div');
    container.style.margin = "10px";
    container.innerHTML = `<strong>${found.pt}</strong>`;
    container.appendChild(iconElement);
    
    // Adiciona o container ao body (ou a outro elemento de sua preferência)
    document.body.appendChild(container);
  } else {
    console.log(item, "não faz parte de facilitiesMap");
  }
}
